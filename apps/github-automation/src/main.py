# ---------- PR body template ------------------------------------------------


def build_pr_body(req: DemoPRRequest, changed_files: list[str]) -> str:
    bullets = "\n".join(f"- `{p}`" for p in changed_files) or "- (none)"
    return (
        "## Summary\n"
        f"{req.body_summary}\n\n"
        "## Changed Files\n"
        f"{bullets}\n\n"
        "## Risk Assessment\n"
        f"{req.risk_assessment}\n\n"
        "## Test Result\n"
        f"{req.test_result}\n\n"
        "## Rollback Plan\n"
        f"{req.rollback_plan}\n"
    )


# ---------- side effects ----------------------------------------------------


async def _publish_notification(
    task_id: str, event_type: str, message: str, *, dry_run: bool
) -> None:
    """Best-effort notification publish. ``dry_run`` is recorded in the message."""
    with contextlib.suppress(Exception):
        client = NotificationClient()
        try:
            notification = client.build_notification(task_id, event_type, message)
            notification["dry_run"] = dry_run
            await client.event_bus.publish_event(NotificationClient.STREAM, notification)
        finally:
            with contextlib.suppress(Exception):
                await client.close()


async def _record_audit(
    task_id: str,
    workflow_id: str,
    summary: str,
    result: str,
    artifact_refs: dict[str, Any],
) -> None:
    """Publish a ``github_automation`` audit event onto ``stream.audit``.

    Stage 19: the unified audit path is ``stream.audit -> audit-worker ->
    audit_logs``. We no longer call ``audit-service`` over HTTP — best-effort
    suppression matches the previous behaviour.
    """
    with contextlib.suppress(Exception):
        await publish_audit_event(
            task_id=task_id,
            workflow_id=workflow_id,
            agent="github-automation",
            decision_type="github_automation",
            summary=summary,
            result=result,
            artifact_refs=artifact_refs,
        )


