@app.post("/github/workflow/demo-pr")
async def demo_pr(req: DemoPRRequest) -> dict:
    """Walk issue → branch → file → PR → checks. Dry-run by default."""
    dry_run = _resolve_dry_run(req.dry_run)
    repo = _resolve_repo(req.repo)
    branch_name = req.branch_name or f"ai-agents-swd/demo-{req.task_id}".replace(" ", "-")

    with start_span(
        "github.demo_pr",
        **{
            "github.repo": repo,
            "github.dry_run": str(dry_run).lower(),
            "github.operation": "demo_pr",
            "task_id": req.task_id,
            "workflow_id": req.workflow_id,
        },
    ):
        client = GitHubClient(repo=repo, dry_run=dry_run)
        result: dict[str, Any] = {
            "dry_run": dry_run,
            "repo": repo,
            "task_id": req.task_id,
            "workflow_id": req.workflow_id,
            "branch_name": branch_name,
            "base_branch": req.base_branch,
            "created_at": _utcnow_iso(),
        }
        try:
            issue = await client.create_issue(
                title=f"[AI-Agents-SWD Test] {req.title}",
                body=req.body_summary,
                task_id=req.task_id,
                workflow_id=req.workflow_id,
            )
            GITHUB_ISSUE_CREATED_TOTAL.labels(dry_run=_dry_run_label(dry_run)).inc()
            result["issue"] = issue.to_dict()

            branch = await client.create_branch(
                branch_name,
                base_branch=req.base_branch,
                task_id=req.task_id,
                workflow_id=req.workflow_id,
            )
            GITHUB_BRANCH_CREATED_TOTAL.labels(dry_run=_dry_run_label(dry_run)).inc()
            result["branch"] = branch.to_dict()

            file = await client.create_or_update_file(
                req.file_path,
                req.file_content,
                message=f"[AI-Agents-SWD Test] add {req.file_path}",
                branch=branch_name,
                task_id=req.task_id,
                workflow_id=req.workflow_id,
            )
            result["file"] = file.to_dict()

            pr_body = build_pr_body(req, [req.file_path])
            pr = await client.create_pull_request(
                title=req.title,
                body=pr_body,
                head_branch=branch_name,
                base_branch=req.base_branch,
                task_id=req.task_id,
                workflow_id=req.workflow_id,
            )
            GITHUB_PR_CREATED_TOTAL.labels(dry_run=_dry_run_label(dry_run)).inc()
            result["pull_request"] = pr.to_dict()

            checks = await client.read_checks(
                branch_name,
                task_id=req.task_id,
                workflow_id=req.workflow_id,
            )
            GITHUB_CHECKS_READ_TOTAL.labels(dry_run=_dry_run_label(dry_run)).inc()
            result["checks"] = checks.to_dict()
        except GitHubClientError as exc:
            GITHUB_AUTOMATION_FAILURES_TOTAL.labels(operation="demo_pr").inc()
            raise HTTPException(
                status_code=exc.status_code or 502,
                detail={"operation": "demo_pr", "error": str(exc)},
            ) from exc

        # Side effects: notification + audit. Best-effort.
        event_type = "github.pr.dry_run" if dry_run else "github.pr.created"
        message = (
            f"github-automation demo-pr completed (dry_run={dry_run}); "
            f"issue={result['issue'].get('url', '')} "
            f"pr={result['pull_request'].get('url', '')}"
        )
        await _publish_notification(req.task_id, event_type, message, dry_run=dry_run)
        await _record_audit(
            task_id=req.task_id,
            workflow_id=req.workflow_id,
            summary=f"GitHub demo PR ({'dry-run' if dry_run else 'live'})",
            result="ok",
            artifact_refs={
                "issue_url": result["issue"].get("url", ""),
                "branch": branch_name,
                "pr_url": result["pull_request"].get("url", ""),
                "dry_run": dry_run,
            },
        )
        result["event_type"] = event_type
        return result


