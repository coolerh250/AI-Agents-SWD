"""github-automation service.

Stage 17 GitHub automation foundation. The service runs **dry-run by
default** — every call to ``/github/...`` returns a deterministic mock
response and contacts no real GitHub API unless the request explicitly
sets ``dry_run=false`` *and* the container has a ``GITHUB_TOKEN`` env
var. The token is never logged, never echoed in responses, never
written to any artifact.

The aggregate endpoint ``POST /github/workflow/demo-pr`` walks the
issue → branch → file → PR → checks flow, writes an audit event, and
publishes a notification, so the platform has a single in-cluster
smoke for the GitHub automation pipeline.
"""

