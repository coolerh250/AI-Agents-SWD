_DEFAULT_PROVIDER: SecretProvider | None = None


def provider_from_env(env: Mapping[str, str] | None = None) -> SecretProvider:
    """Construct a SecretProvider based on ``SECRET_PROVIDER``.

    Unknown values fall back to :class:`EnvSecretProvider` — the safe
    default. The factory NEVER raises; callers can trust they will get
    a working (possibly safe-degraded) provider back.
    """
    snapshot = env if env is not None else os.environ
    choice = (snapshot.get("SECRET_PROVIDER") or "env").strip().lower()
    if choice == "vault":
        return VaultKvSecretProvider()
    if choice == "mock-vault":
        return MockVaultSecretProvider()
    return EnvSecretProvider()


def default_provider() -> SecretProvider:
    """Return the singleton SecretProvider for the current process.

    Tests reset the singleton by importing this module and clearing
    ``_DEFAULT_PROVIDER``; production code just calls the function.
    """
    global _DEFAULT_PROVIDER
    if _DEFAULT_PROVIDER is None:
        _DEFAULT_PROVIDER = provider_from_env()
    return _DEFAULT_PROVIDER


def reset_default_provider() -> None:
    """Drop the singleton so the next ``default_provider()`` call rebuilds."""
    global _DEFAULT_PROVIDER
    _DEFAULT_PROVIDER = None


# Detection helper used by the validator + tests to fail loudly when a
# placeholder slips into a "live" env.
_PLACEHOLDER_REGEX = re.compile(re.escape(_PLACEHOLDER_MARKER))


def looks_like_placeholder(value: str | None) -> bool:
    if not value:
        return False
    return bool(_PLACEHOLDER_REGEX.search(value))
